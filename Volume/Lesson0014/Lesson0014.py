import matplotlib.pyplot as plt
import numpy as np

# Tạo mảng số từ 0 đến 10 với bước nhảy là 0.1
x = np.arange(0, 10, 0.1)

# Tính y = sin(x)
y = np.sin(x)

# Tạo đồ thị
plt.plot(x, y)

# Thêm tiêu đề cho đồ thị
plt.title('Đồ thị y = sin(x)')

# Thêm nhãn cho trục x và y
plt.xlabel('x')
plt.ylabel('y')

# Hiện lưới trên đồ thị
plt.grid(True)

# Hiện đồ thị
plt.show()

